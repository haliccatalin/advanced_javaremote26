package com.sda.collections.exercises;

public class Friend {

    private String name;
    private String dateOfBirth;

    public Friend(String name, String dateOfBirth) {
        this.name = name;
        this.dateOfBirth = dateOfBirth;
    }

    public String getName() {
        return name;
    }

    public String getDateOfBirth() {
        return dateOfBirth;
    }
}